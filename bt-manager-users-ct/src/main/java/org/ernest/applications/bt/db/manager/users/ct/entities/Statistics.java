package org.ernest.applications.bt.db.manager.users.ct.entities;

public class Statistics {

	private int resistence;
	private int sprint;
	private int montain;
	private int flat;
	private int btt;
	private int road;
	
	public int getResistence() {
		return resistence;
	}
	public void setResistence(int resistence) {
		this.resistence = resistence;
	}
	public int getSprint() {
		return sprint;
	}
	public void setSprint(int sprint) {
		this.sprint = sprint;
	}
	public int getMontain() {
		return montain;
	}
	public void setMontain(int montain) {
		this.montain = montain;
	}
	public int getFlat() {
		return flat;
	}
	public void setFlat(int flat) {
		this.flat = flat;
	}
	public int getBtt() {
		return btt;
	}
	public void setBtt(int btt) {
		this.btt = btt;
	}
	public int getRoad() {
		return road;
	}
	public void setRoad(int road) {
		this.road = road;
	}
	
	

}
